    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, UnknownHostException {
        ExcelPatern geraExcel = new ExcelPatern();
        String ini=request.getParameter("inicio");
        String fim= request.getParameter("fim");
        String sistema= request.getParameter("sistema");
        try {
            if (fim.equalsIgnoreCase("")&& ini.equalsIgnoreCase("")&& sistema.equalsIgnoreCase("")){
                geraExcel.executa();
            }
            else{
                geraExcel.executaData(ini, fim,sistema);
            }
        } catch (ClassNotFoundException | ParseException ex) {
            Logger.getLogger(ExtractionExcelPaternServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
         getUser user = new getUser();
        response.setContentType("text/html;charset=UTF-8");
        String filePath = "C:\\Users\\"+ System.getProperty("user.name")+"\\Downloads\\Incidentes.xls";
        File downloadFile = new File(filePath);
        FileInputStream inStream = new FileInputStream(downloadFile);
         
        // if you want to use a relative path to context root:
        String relativePath = getServletContext().getRealPath("");
        System.out.println("relativePath = " + relativePath);
         
        // obtains ServletContext
        ServletContext context = getServletContext();
         
        // gets MIME type of the file
        String mimeType = context.getMimeType(filePath);
        if (mimeType == null) {        
            // set to binary type if MIME mapping not found
            mimeType = "application/octet-stream";
        }
        System.out.println("MIME type: " + mimeType);
         
        // modifies response
        response.setContentType(mimeType);
        response.setContentLength((int) downloadFile.length());
         
        // forces download
        String headerKey = "Content-Disposition";
        String headerValue = String.format("attachment; filename=\"%s\"", downloadFile.getName());
        response.setHeader(headerKey, headerValue);
         
        // obtains response's output stream
        OutputStream outStream = response.getOutputStream();
         
        byte[] buffer = new byte[4096];
        int bytesRead = -1;
         
        while ((bytesRead = inStream.read(buffer)) != -1) {
            outStream.write(buffer, 0, bytesRead);
        }
         
        inStream.close();
        outStream.close();     
     
 
        processRequest(request, response);

    }

